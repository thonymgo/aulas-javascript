// Aqui teremos alguns exemplos de como utilizar o console do javascript

// Log - o Log é utilizado para imprimir valores passados para ele através de strings, numbers ou variáveis.
console.log("Olá mundo, estou imprimindo um valor de uma string");

var nome = "Anthony"

console.log(nome); // A saída será Anthony

var idade = 11; 

console.log(idade); // A saída será 11

// Exemplo com concatenação

console.log("Olá, " + nome + " a sua idade é :  " + idade);  // a saída será Olá Anthony, a sua idade é: 11